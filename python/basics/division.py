#write a simple python program to perform all division operations# using the division operator (/), floor division operator (//), and modulus operator (%)
# Get user input for two numbers
num1 = float(input("Enter the first number: "))
num2 = float(input("Enter the second number: "))

division = num1 / num2
floor_division = num1 // num2
modulus = num1 % num2

print("Division by /:", division)
print("Floor Division (//):", floor_division)
print("Modulus (%):", modulus)

